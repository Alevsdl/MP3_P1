        Cancion aux = (Cancion) modeloLista.getElementAt(index);
        this.lbTitulo.setText(aux.getTitulo());
        this.lbArtista.setText(aux.getArtista());
        this.lbAlbum.setText(aux.getAlbum());
        this.lbAnio.setText(aux.getAnio());
